
CREATE TABLE REGIAO
(
    ID_REGIAO NUMERIC PRIMARY KEY ,
    NOME VARCHAR (50) NOT NULL    
)


CREATE TABLE ESTADO
(
    ID_ESTADO NUMERIC NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    ID_REGIAO NUMERIC NOT NULL,
    CONSTRAINT FK_REGIAO FOREIGN KEY (ID_REGIAO) REFERENCES REGIAO(ID_REGIAO),
    CONSTRAINT PK_ESTADO PRIMARY KEY (ID_ESTADO)
)

CREATE TABLE CIDADE
(
    ID_CIDADE NUMERIC NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    ID_ESTADO NUMERIC NOT NULL,
    CONSTRAINT FK_ESTADO FOREIGN KEY (ID_ESTADO) REFERENCES ESTADO(ID_ESTADO),
    CONSTRAINT PK_CIDADE PRIMARY KEY (ID_CIDADE)
)

CREATE TABLE BAIRRO
(
    ID_BAIRRO NUMERIC NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    ID_CIDADE NUMERIC NOT NULL,
    CONSTRAINT FK_CIDADE FOREIGN KEY (ID_CIDADE) REFERENCES CIDADE(ID_CIDADE),
    CONSTRAINT PK_BAIRRO PRIMARY KEY (ID_BAIRRO)
)


CREATE TABLE USUARIO
(
    ID_USUARIO NUMERIC NOT NULL,
    USER_NAME VARCHAR(15),
    PASSWORD VARCHAR(255),
    CONSTRAINT FK_USUARIO PRIMARY KEY (ID_USUARIO)
)

CREATE TABLE PESSOA
(
    ID_PESSOA NUMERIC NOT NULL PRIMARY KEY,
    NOME VARCHAR(200),
    RG VARCHAR(11),
    CPF_CNPJ VARCHAR(20),
    ID_USUARIO NUMERIC NOT NULL,
    CONSTRAINT FK_USUARIO_PESSOA FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO (ID_USUARIO)
)

CREATE TABLE ENDERECO   
(
    ID_ENDERECO NUMERIC NOT NULL,
    COMPLEMENTO VARCHAR(255),
    REFERENCIA VARCHAR(255),
    TELEFONE VARCHAR(20),
    ID_BAIRRO NUMERIC NOT NULL,
    ID_PESSOA NUMERIC NOT NULL,
    CONSTRAINT FK_BAIRRO_ENDERECO FOREIGN KEY (ID_BAIRRO) REFERENCES BAIRRO (ID_BAIRRO),
    CONSTRAINT PK_ENDERECO PRIMARY KEY (ID_ENDERECO),
    CONSTRAINT FK_PESSOA FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA (ID_PESSOA)
)

CREATE TABLE AUTOR
    (
        ID_AUTOR NUMERIC PRIMARY KEY,
        NOME VARCHAR(200)
    )
    
CREATE TABLE EDITORA
(
    ID_EDITORA NUMERIC PRIMARY KEY,
    DESCRICAO VARCHAR(200)
)

CREATE TABLE LIVRO
(
    ID_LIVRO NUMERIC PRIMARY KEY,
    NOME VARCHAR(200),
    EDICAO VARCHAR(200),
    ANO_PUBLICACAO DATE,
    ID_AUTOR NUMERIC NOT NULL,
    ID_EDITORA NUMERIC NOT NULL,
    CONSTRAINT FK_EDITORA FOREIGN KEY (ID_EDITORA) REFERENCES EDITORA(ID_EDITORA),
    CONSTRAINT FK_AUTOR FOREIGN KEY (ID_AUTOR) REFERENCES AUTOR(ID_AUTOR)
)

CREATE TABLE ACERVO
    (
        ID_ACERVO NUMERIC NOT NULL PRIMARY KEY,
        ID_PESSOA NUMERIC NOT NULL,
        ID_LIVRO NUMERIC NOT NULL,
        CONSTRAINT FK_PESSOA_ACERVO FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA(ID_PESSOA),
        CONSTRAINT FK_LIVRO_ACERVO FOREIGN KEY (ID_LIVRO) REFERENCES LIVRO(ID_LIVRO)
    )

CREATE TABLE TIPO_NEGOCIACAO
    (
        ID_TIPO_NEGOCIACAO NUMERIC NOT NULL PRIMARY KEY,
        DESCRICAO VARCHAR(100)
    )

CREATE TABLE NEGOCIACAO
(
    ID_NEGOCIACAO NUMERIC NOT NULL PRIMARY KEY,
    ID_ACERVO NUMERIC NOT NULL,
    ID_TIPO_NEGOCIACAO NUMERIC NOT NULL,
    VALOR DECIMAL,
    CONSTRAINT FK_ACERVO_NEGOCIACAO FOREIGN KEY (ID_ACERVO) REFERENCES ACERVO (ID_ACERVO),
    CONSTRAINT FK_TIPO_NEGOCIACAO FOREIGN KEY (ID_TIPO_NEGOCIACAO) REFERENCES TIPO_NEGOCIACAO (ID_TIPO_NEGOCIACAO)
)

CREATE TABLE PEDIDO_COMPRA
(
    ID_PEDIDO_COMPRA NUMERIC NOT NULL PRIMARY KEY,
    ID_PESSOA NUMERIC NOT NULL,
    CONSTRAINT FK_PESSOA_PEDIDO_COMPRA FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA (ID_PESSOA)
)
CREATE TABLE ITEM_PEDIDO_COMPRA
    (
        ID_ITEM_PEDIDO_COMPRA NUMERIC NOT NULL PRIMARY KEY,
        ID_NEGOCIACAO NUMERIC NOT NULL,
        ID_PEDIDO_COMPRA NUMERIC NOT NULL,
        CONSTRAINT FK_NEGOCIACAO_ITEM_PEDIDO FOREIGN KEY (ID_NEGOCIACAO) REFERENCES NEGOCIACAO (ID_NEGOCIACAO),
        CONSTRAINT FK_PEDIDO_COMPRA_ITEM FOREIGN KEY (ID_PEDIDO_COMPRA) REFERENCES PEDIDO_COMPRA (ID_PEDIDO_COMPRA)
    )




